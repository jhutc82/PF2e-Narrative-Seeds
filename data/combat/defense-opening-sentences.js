/**
 * Helper function to get defense-aware opening sentence
 * @param {string} outcome - 'failure' or 'criticalFailure'
 * @param {string} missReason - 'dodge', 'armor', 'shield', 'naturalArmor', or 'miss'
 * @param {string} detailLevel - Currently only 'cinematic' implemented
 * @returns {Array} Array of appropriate opening sentences
 */
export function getDefenseOpenings(outcome, missReason, detailLevel = 'cinematic') {
  if (detailLevel !== 'cinematic') {
    return []; // Other detail levels not yet implemented
  }

  if (outcome === 'failure') {
    return CINEMATIC_DEFENSE_OPENINGS_FAILURE[missReason] || CINEMATIC_DEFENSE_OPENINGS_FAILURE.miss;
  } else if (outcome === 'criticalFailure') {
    return CINEMATIC_DEFENSE_OPENINGS_CRIT_FAILURE[missReason] || CINEMATIC_DEFENSE_OPENINGS_CRIT_FAILURE.miss;
  }

  return [];
}
