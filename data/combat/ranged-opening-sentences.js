/**
 * Helper function to get appropriate ranged opening sentences
 * @param {string} weaponCategory - 'bow', 'crossbow', 'thrown', 'firearm', 'bomb', 'spell'
 * @param {string} detailLevel - 'cinematic', 'detailed', 'standard'
 * @param {string} outcome - 'criticalSuccess', 'success', 'failure', 'criticalFailure'
 * @param {Object} context - Context object with attackerName, targetName, etc.
 * @returns {string|null}
 */
export function getRangedOpeningSentence(weaponCategory, detailLevel, outcome, context = {}) {
  let openings;

  // Select the appropriate openings based on weapon category and detail level
  switch(weaponCategory) {
    case 'bow':
      openings = BOW_CINEMATIC_OPENINGS[outcome]; // For now, using cinematic
      break;
    case 'crossbow':
      openings = CROSSBOW_CINEMATIC_OPENINGS[outcome];
      break;
    case 'thrown':
      openings = THROWN_CINEMATIC_OPENINGS[outcome];
      break;
    case 'firearm':
      openings = FIREARM_CINEMATIC_OPENINGS[outcome];
      break;
    case 'bomb':
      openings = BOMB_CINEMATIC_OPENINGS[outcome];
      break;
    case 'spell':
      openings = SPELL_CINEMATIC_OPENINGS[outcome];
      break;
    // Additional weapon categories will be added
    default:
      return null;
  }

  if (!openings || openings.length === 0) {
    return null;
  }

  // Select a random opening
  const template = openings[Math.floor(Math.random() * openings.length)];

  // Replace placeholders with actual values
  return interpolateTemplate(template, context);
}

/**
 * Interpolate template with context values
 * @param {string} template - Template string with ${variable} placeholders
 * @param {Object} context - Object with values to interpolate
 * @returns {string}
 */
function interpolateTemplate(template, context) {
  return template.replace(/\$\{(\w+)\}/g, (match, key) => {
    return context[key] !== undefined ? context[key] : match;
  });
}
